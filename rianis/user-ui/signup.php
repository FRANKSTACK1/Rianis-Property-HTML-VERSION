<script>
        // Password strength and validation logic
        const passwordInput = document.getElementById('password');
        const confirmInput = document.getElementById('confirm_password');
        const indicators = [
            document.getElementById('strength-1'),
            document.getElementById('strength-2'),
            document.getElementById('strength-3'),
            document.getElementById('strength-4')
        ];

        passwordInput.addEventListener('input', () => {
            const val = passwordInput.value;
            let strength = 0;
            if (val.length >= 6) strength++;
            if (val.length >= 8) strength++;
            if (/[A-Z]/.test(val) && /[0-9]/.test(val)) strength++;
            if (/[^A-Za-z0-9]/.test(val)) strength++;

            indicators.forEach((ind, i) => {
                if (i < strength) {
                    ind.className = 'flex-1 rounded-full transition-colors ' + 
                        (strength <= 1 ? 'bg-error' : strength <= 2 ? 'bg-tertiary' : strength <= 3 ? 'bg-secondary-container' : 'bg-secondary');
                } else {
                    ind.className = 'flex-1 bg-surface-variant rounded-full transition-colors';
                }
            });
        });

        // Match validation logic
        confirmInput.addEventListener('input', () => {
            if (confirmInput.value !== passwordInput.value) {
                confirmInput.style.borderColor = '#ba1a1a'; // Error color from config
            } else {
                confirmInput.style.borderColor = '#006c4a'; // Secondary color from config
            }
        });

        document.getElementById('registrationForm').addEventListener('submit', (e) => {
            if (confirmInput.value !== passwordInput.value) {
                e.preventDefault();
                alert('Passwords do not match. Please ensure both fields are identical.');
            }
        });
    </script>
